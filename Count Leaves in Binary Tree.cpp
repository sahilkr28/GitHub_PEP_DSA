// Output: 3
// Explanation: Three leaves are 3, 5 and 1.
// Input:
// Given Tree is

// Output: 4
// Explanation: Four leaves are 20, 40, 65 and 80.
// Input:
// Given Tree is  

// Output: 3
// Explanation: Three leaves are 20, 27 and 40.
//  Constraints:
// 1<= number of nodes <= 105
// 1<= node->data <= 105

#include <bits/stdc++.h>
using namespace std;

struct Node
{
    int data;
    Node* left;
    Node* right;
};

class Solution {
  public:
    // Function to count the number of leaf nodes in a binary tree.
    int countLeaves(Node* root) {
        // write code here
        if (!root) return 0;

        int count = 0;
        stack<Node*> st;
        st.push(root);

        while (!st.empty()) {
            Node* curr = st.top();
            st.pop();

            // Check if leaf node
            if (!curr->left && !curr->right) {
                count++;
            }

            // Push children
            if (curr->right) st.push(curr->right);
            if (curr->left)  st.push(curr->left);
        }

        return count;
    }
};